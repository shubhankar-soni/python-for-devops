num1 = 10
num2 = 5

def addition(num1, num2):
    add = num1 + num2
    return add

def subtraction(num1, num2):
    sub = num1 - num2
    return sub

def multiplication(num1, num2):
    mul = num1 * num2
    return mul

def division(num1, num2):
    division = num1 / num2
    return division

